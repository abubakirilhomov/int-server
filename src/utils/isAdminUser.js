/**
 * Единый источник истины «является ли пользователь админом».
 *
 * Админ-права ортогональны к `role`: канонический признак — флаг `isAdmin`.
 * Значение `role === "admin"` поддерживается как легаси (чистые админ-аккаунты,
 * не являющиеся учебными менторами) — миграция БД не требуется.
 *
 * Работает одинаково и на decoded JWT (`req.user`), и на mongoose-доке ментора —
 * форма полей совпадает (`role`, `isAdmin`).
 */
module.exports = (u) => !!(u && (u.isAdmin === true || u.role === "admin"));
